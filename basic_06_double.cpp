#include <iostream>

int main() {
    double d1 = 1/2.0;
    std::cout << d1 << std::endl;
    // 0.5

    double d2 = 1.0/2;
    std::cout << d2 << std::endl;
    // 0.5

    double d3 = 1/2;
    std::cout << d3 << std::endl;
    // 0

    double d4 = 5.3;
    std::cout << d4 << std::endl;
    // 5.3

    return 0;
}
